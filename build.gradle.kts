import proguard.gradle.ProGuardTask

buildscript {
    repositories { mavenCentral() }
    dependencies { classpath("com.guardsquare:proguard-gradle:7.9.1") }
}

plugins {
    application
}

group = "com.tonicbox.dukes8bit"
version = "1.0"

java {
    toolchain {
        languageVersion = JavaLanguageVersion.of(25)
    }
}

// The game ships as a pack200 blob and is reconstructed at runtime by the JDK-11 built-in Pack200 unpacker. pack200
// was removed from the JDK in 14, so the build packs with (and `run` launches on) a JDK 11 toolchain - found among
// installed JDKs or auto-provisioned via the Foojay resolver (see settings.gradle.kts).
val java11 = javaToolchains.launcherFor { languageVersion = JavaLanguageVersion.of(11) }
val pack200Tool = if (System.getProperty("os.name").startsWith("Windows")) "bin/pack200.exe" else "bin/pack200"
val pack200Exe = java11.map { it.metadata.installationPath.file(pack200Tool).asFile.absolutePath }

application {
    // Game ships as a compressed resource (see the proguard task); the Main loader inflates and launches it.
    mainClass = "Main"
}

// Main is compiled on its own (see compileMain) by the JDK-11 toolchain so it can call java.util.jar.Pack200
// directly rather than reflectively (the API was removed in JDK 14). Keep it out of the JDK-25 game compile.
sourceSets {
    main {
        java {
            exclude("Main.java")
        }
    }
}

repositories {
    mavenCentral()
}

dependencies {
    testImplementation("org.junit.jupiter:junit-jupiter:5.11.4")
    testRuntimeOnly("org.junit.platform:junit-platform-launcher")
}

tasks.test {
    useJUnitPlatform()
    // Constructing Game calls Sound.init(); point the MIDI synth at a name that does not resolve so the synth
    // fails to open and Sound becomes a no-op - otherwise its non-daemon scheduler keeps the test JVM alive.
    systemProperty("javax.sound.midi.Synthesizer", "#none")
    systemProperty("java.awt.headless", "true")
}

tasks.withType<JavaCompile>().configureEach {
    options.release = 25
    options.isDebug = false
    options.encoding = "UTF-8"
    // De-sugar string concatenation to StringBuilder instead of invokedynamic, dropping the BootstrapMethods
    // block + 23 indy sites + the StringConcatFactory handle from the merged class.
    options.compilerArgs.add("-XDstringConcat=inline")
}

// ProGuard minifies in place, so feeding already-minified classes back in would double-minify and drift the
// byte count. Clean-recompile each run to keep the minify input pristine and `size` deterministic.
tasks.named<JavaCompile>("compileJava") {
    outputs.upToDateWhen { false }
    doFirst { destinationDirectory.get().asFile.deleteRecursively() }
}

// Compile the Main loader on the JDK-11 toolchain (which still has java.util.jar.Pack200), into its own output dir.
// The proguard task copies the result into the shipped classes after minifying the game. release = 11 overrides the
// global release = 25 set in the withType<JavaCompile> block above (registered first, so this wins).
val compileMain = tasks.register<JavaCompile>("compileMain") {
    source = fileTree(layout.projectDirectory.dir("src/main/java")) { include("Main.java") }
    classpath = files()
    destinationDirectory = layout.buildDirectory.dir("classes/main11")
    javaCompiler = javaToolchains.compilerFor { languageVersion = JavaLanguageVersion.of(11) }
    options.release = 11
}

// The compiled-classes directory the bytecode passes transform and ProGuard later minifies in place.
val classesDir = layout.buildDirectory.dir("classes/java/main").get().asFile

// Runs the build-time bytecode passes (see buildSrc dukes.yabr.BuildPasses) over the freshly compiled classes,
// before the jar and ProGuard:
// - pack each class's constant Color fields into a decoded palette
// - Method Inlining. The inline list comes from inline-allowlist.txt (generated by tools/tune-inline.sh) or -Pinline=Game#a,Renderer#b for one-off experiments.
val allowlistFile = layout.projectDirectory.file("inline-allowlist.txt").asFile
val explicitInline: List<String>? = (findProperty("inline") as String?)
    ?.split(",")?.map { it.trim() }?.filter { it.isNotEmpty() }
val transformClasses = tasks.register("transformClasses") {
    dependsOn("compileJava")
    // The passes (and ProGuard after) mutate build/classes/java/main in place, which is also the test classpath,
    // so let `test` run against the pristine compiled classes first whenever both are in the graph.
    mustRunAfter("test")
    outputs.upToDateWhen { false }
    doLast {
        val allowlist = explicitInline ?: if (allowlistFile.exists())
            allowlistFile.readLines().map { it.trim() }.filter { it.isNotEmpty() && !it.startsWith("//") }
        else emptyList()
        dukes.yabr.BuildPasses.apply(classesDir, allowlist)
    }
}

// Lists every private single-call candidate (on the freshly compiled, pre-transform classes) for the tuner.
tasks.register("listInlineCandidates") {
    dependsOn("compileJava")
    doLast {
        dukes.yabr.inline.MethodInliner.discoverCandidates(classesDir).sorted().forEach { println(it) }
    }
}

// No runtime deps, so the plain jar is already runnable. Force a repackage each run so ProGuard minifies
// freshly-compiled classes rather than re-minifying a previous jar.
tasks.jar {
    archiveBaseName = "DukesDescent"
    manifest { attributes["Main-Class"] = "Main" }
    dependsOn(transformClasses)
    outputs.upToDateWhen { false }
    // Because I'm dumb and break my own builds by having cli open in the libs dir and jar isn't able to remove the directory
    doFirst {
        val jar = archiveFile.get().asFile
        if (jar.exists() && !jar.delete()) {
            throw GradleException("Could not delete a stale ${jar.name} - it is held open by another process "
                + "(a running game window, or a leftover Gradle daemon). Close it and rebuild.")
        }
    }
}

tasks.register<ProGuardTask>("proguard") {
    group = "build"
    description = "Minify in place (shrink + shorten members, keep class names): the jar and compiled classes."
    dependsOn("jar", "compileMain")
    // Always re-run: it overwrites the compile/jar outputs, which would confuse up-to-date checks.
    outputs.upToDateWhen { false }

    val jdk = System.getProperty("java.home")
    val jarFile = layout.buildDirectory.file("libs/DukesDescent-$version.jar").get().asFile
    val tmpJar = layout.buildDirectory.file("tmp/proguard-min.jar").get().asFile

    injars(jarFile)
    outjars(tmpJar)
    libraryjars("$jdk/jmods/java.base.jmod")
    libraryjars("$jdk/jmods/java.desktop.jmod")
    libraryjars("$jdk/jmods/jdk.unsupported.jmod")

    // Game is reached reflectively, so keep its main() as the shrink root (everything it uses is retained, members
    // still shorten). Main is not in the injar - it is compiled separately (compileMain) and injected below.
    keep("class Game { static void main(); }")

    // Keep class names readable; members may shorten. Two optimizations are off because they grow this build:
    // unique-method inlining (cascades the program into Main, ~+13 KB) and parameter propagation (~+17 B).
    // 8 passes is where the optimizer converges.
    keepnames("class **")
    overloadaggressively()
    allowaccessmodification()
    optimizationpasses(8)
    optimizations("!class/merging/*,!method/inlining/unique,!method/propagation/*")
    dontwarn()
    dontnote()

    doLast {
        // Replace the compiled classes with ProGuard's minified output (the classes are what the size metric measures).
        classesDir.deleteRecursively()
        copy {
            from(zipTree(tmpJar)) { include("**/*.class") }
            into(classesDir)
        }
        tmpJar.delete()
        // Drop in the JDK-11-compiled Main loader (it bypasses ProGuard - its direct Pack200 calls don't resolve
        // against the JDK-25 library jars). LoaderMinifier strips its <init> below.
        copy {
            from(layout.buildDirectory.dir("classes/main11"))
            into(classesDir)
        }
        // Frame-strip the minified Game, deflate it into the "Game" resource, and drop the .class: the Main
        // loader inflates and defines it at startup. The shipped classes are just Main.
        val resourcesDir = layout.buildDirectory.dir("resources/main").get().asFile
        val gameClass = File(classesDir, "Game.class")
        val gameBlob = File(resourcesDir, "G")
        val blobBytes = dukes.yabr.pack.ResourcePacker.pack(gameClass, gameBlob, pack200Exe.get())
        // Strip the dead no-arg constructor from the raw-shipped Main loader (it is never instantiated).
        dukes.yabr.pack.LoaderMinifier.minify(File(classesDir, "Main.class"))
        // Repackage the runnable jar to match: the Main loader plus the Game blob, no Game.class. We write the
        // ZIP ourselves (dukes.yabr.pack.JarPacker) so each entry uses OptimalDeflate - which beats the stock
        // Deflater on the class bytes - and the already-compressed Game blob is stored rather than re-deflated.
        val manifestBytes = "Manifest-Version: 1.0\r\nMain-Class: Main\r\n\r\n".toByteArray()
        val jarEntries = mutableListOf(dukes.yabr.pack.JarPacker.Entry("META-INF/MANIFEST.MF", manifestBytes))
        listOf(classesDir, resourcesDir).forEach { root ->
            root.walkTopDown().filter { it.isFile }.forEach { file ->
                jarEntries.add(dukes.yabr.pack.JarPacker.Entry(
                    file.relativeTo(root).invariantSeparatorsPath, file.readBytes()))
            }
        }
        dukes.yabr.pack.JarPacker.write(jarFile, jarEntries)
        val classBytes = classesDir.walkTopDown().filter { it.isFile }.sumOf { it.length() }
        println("Minified -> classes %d B + Game blob %d B (%.2f KB total), jar %d B  %s"
            .format(classBytes, blobBytes, (classBytes + blobBytes) / 1024.0, jarFile.length(), jarFile.name))
    }
}

// Build goes through ProGuard, so the jar and the shipped classes are minified.
tasks.named("build") { dependsOn("proguard") }

// `run` plays the real shipped artifact: launch Main from the packed jar on the JDK 11 toolchain (whose built-in
// Pack200 unpacker rebuilds the game class). The default application `run` would use the raw build/classes on the
// build JDK, where the game is still unpacked and Pack200 is absent, so it is repointed at the jar and JDK 11.
tasks.named<JavaExec>("run") {
    dependsOn("proguard")
    classpath = files(layout.buildDirectory.file("libs/DukesDescent-$version.jar"))
    mainClass = "Main"
    javaLauncher = java11
}

tasks.register("size") {
    group = "verification"
    description = "Reports total runtime size (minified classes + resources) and the packaged jar size."
    dependsOn("proguard")
    doLast {
        val measured = listOf(
            layout.buildDirectory.dir("classes/java/main").get().asFile,
            layout.buildDirectory.dir("resources/main").get().asFile,
        )
        var totalBytes = 0L
        var fileCount = 0
        measured.filter { it.exists() }.forEach { root ->
            root.walkTopDown().filter { it.isFile }.sortedBy { it.path }.forEach { file ->
                totalBytes += file.length()
                fileCount++
                println("%9d B  %s".format(file.length(), file.relativeTo(root.parentFile)))
            }
        }
        val kilobytes = totalBytes / 1024.0
        println("-".repeat(48))
        println("Files: %d".format(fileCount))
        println("Total: %d B (%.2f KB)".format(totalBytes, kilobytes))

        val jarFile = tasks.named<Jar>("jar").get().archiveFile.get().asFile
        if (jarFile.exists()) {
            println("Jar: %d B (%.2f KB)  %s".format(jarFile.length(), jarFile.length() / 1024.0, jarFile.name))
        }
    }
}
